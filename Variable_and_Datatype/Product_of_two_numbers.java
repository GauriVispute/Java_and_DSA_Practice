package Variable_and_Datatype;
import java.util.*;
public class Product_of_two_numbers {
    public static void main(String[] args) {
        @SuppressWarnings("resource")
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b= sc.nextInt();
        int product = a*b;
        System.out.println(product);
    }
}
